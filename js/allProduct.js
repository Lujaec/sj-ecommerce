function shuffleArray(array) {
  const arrCopy = [...array]; // 배열 복사
  for (let i = arrCopy.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [arrCopy[i], arrCopy[j]] = [arrCopy[j], arrCopy[i]]; // 복사된 배열의 요소 교환
  }
  return arrCopy; // 섞인 배열 반환
}

